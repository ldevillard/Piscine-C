#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_program_name(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	char *name;

	(void)argc;
	name = argv[0];
	ft_print_program_name(name);
	ft_putchar('\n');
	return (0);
}
